<div class="flex flex-col gap-4 p-4">
	<div>
		Scopes are meaningful parts of the problem that can be completed independently and in a short
		period of time. They represent a demonstrable piece of value. They are bigger than tasks but
		much smaller than the overall project.
	</div>
	<div>
		This approach of breaking the overall project into scopes is called <span class="font-bold"
			>'vertical break-down'.
		</span>
	</div>
	<div>
		The opposite would be <span class="font-bold">'horizontal break-down'</span>, in which you would
		break a group of tasks by person, role, by kind of work that needs to be done, layers or
		components that are involved.
	</div>
	<div>
		<span class="font-bold">Avoid it.</span> It would only result in group of disconnected tasks, would
		increase complexity and would hardly be able to accomplish one independently of the other.
	</div>
